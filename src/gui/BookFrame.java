    private void deleteBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBookBtnActionPerformed
        // TODO add your handling code here:
        if(!updateBookIdLabel.getText().equals("")){
            String msg = "Do you want to delete record of book id "+ updateBookIdLabel.getText();
            if(JOptionPane.showConfirmDialog(null, msg) == 0){
                if(ManageBook.isIssused(con, msg)){
                    JOptionPane.showMessageDialog(null, "Book Cannot be Deleted","Book Notification",ERROR_MESSAGE);
                    return;
                }
                if(ManageBook.delete(con, updateBookIdLabel.getText())){
                    JOptionPane.showMessageDialog(null, "Data Deleted Successfully","Book Notification",INFORMATION_MESSAGE);
                    updateTable();
                    resetUpdateForm();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Data Not Deleted","Book Notification",WARNING_MESSAGE);
                }
            }

        }
        else{
            JOptionPane.showMessageDialog(null, "Enter a valid Book id","Book Notification",ERROR_MESSAGE);

        }
    }//GEN-LAST:event_deleteBookBtnActionPerformed

