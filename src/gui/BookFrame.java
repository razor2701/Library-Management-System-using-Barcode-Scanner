    public boolean checkFields(){
        int flag = 1;
        String message = "";
        
        if(bookidTf.getText().length() <  13 || bookidTf.getText().length() >  13){
            flag =0;
            message +="Enter A Valid BookId \n";
        }
        
        if(booknameTf.getText().isEmpty()){
            flag = 0;
            message +="Enter A Valid Book Name\n";
        }
        if(isbnTf.getText().isEmpty()){
            flag = 0;
            message +="Enter A Valid ISBN\n";
        }
        if(authorTf.getText().isEmpty()){
            flag = 0;
            message +="Enter A Valid Author Name\n";
        }
        if(editionTf.getText().isEmpty()){
            flag = 0;
            message +="Enter A Valid Edition \n";
        }
        if(nopTf.getText().isEmpty() ){
            flag = 0;
            message +="Enter A Valid Pages\n";
        }
        if(flag == 1){
            return true;
        }
        JOptionPane.showMessageDialog(null ,message, "Input Error", ERROR_MESSAGE);
        return false;
    }
