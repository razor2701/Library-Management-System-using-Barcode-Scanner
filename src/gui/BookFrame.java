    private void updateShelfBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateShelfBtnActionPerformed
        // TODO add your handling code here:
        if(!updateShelfNoJlabel.getText().equals("")){
            if(!updateShelfLocation.getText().equals("")){
                if( ManageBook.updateShelf(con,updateShelfNoJlabel.getText() , updateShelfLocation.getText()) )
                {
                    updateTable();
                    updateShelfNoJlabel.setText("");
                    updateShelfLocation.setText("");
                    JOptionPane.showMessageDialog(null, "Shelf Updated Successfully","Shelf Updated",INFORMATION_MESSAGE);
                }
                else{

                    JOptionPane.showMessageDialog(null, "Shelf  Not Updated","Shelf Updated",INFORMATION_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "The Location Field cannot be Blank","Shelf", ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Select a Valid Shelf No","Shelf Error",ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateShelfBtnActionPerformed

